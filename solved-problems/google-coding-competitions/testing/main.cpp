#include <bits/stdc++.h>

void solve_test_case(const int test_case) {
  std::cout << "Case #" << test_case << ": "
            << "" << '\n';
}

int32_t main() {
  std::ios_base::sync_with_stdio(0);
  std::cin.tie(0);
  std::cout.tie(0);

  int T;
  std::cin >> T;
  for (int test = 1; test <= T; test += 1) {
    solve_test_case(test);
  }

  return 0;
}